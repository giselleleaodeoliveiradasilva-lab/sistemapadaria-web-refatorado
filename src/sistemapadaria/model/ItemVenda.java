package sistemapadaria.model;

/**
 *
 * @author eberf
 */
public class ItemVenda {
      private int id;
    private String produto; // Armazena o nome do produto no momento da venda (opcional, mas útil)
    private int quantidade;
    private double precoUnitario;
    private double subTotal;
    private int idVenda;   // Chave estrangeira para a tabela 'venda' (id_venda)
    private int idProduto; // Chave estrangeira para a tabela 'produto' (id_produto)

    // Construtor Padrão
    public ItemVenda() {
    }

    // --- Métodos Getters e Setters ---

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProduto() {
        return produto;
    }

    public void setProduto(String produto) {
        this.produto = produto;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }

    public double getPrecoUnitario() {
        return precoUnitario;
    }

    public void setPrecoUnitario(double precoUnitario) {
        this.precoUnitario = precoUnitario;
    }

    public double getSubTotal() {
        return subTotal;
    }

    public void setSubTotal(double subTotal) {
        this.subTotal = subTotal;
    }

    public int getIdVenda() {
        return idVenda;
    }

    public void setIdVenda(int idVenda) {
        this.idVenda = idVenda;
    }

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }
}
